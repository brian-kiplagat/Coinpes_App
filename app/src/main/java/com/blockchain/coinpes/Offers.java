package com.blockchain.coinpes;

public class Offers {
    private String offer_string;
    private String positive_feedback;
    private String offer_user;

    public String getOffer_string() {
        return offer_string;
    }

    public void setOffer_string(String offer_string) {
        this.offer_string = offer_string;
    }

    public String getPositive_feedback() {
        return positive_feedback;
    }

    public void setPositive_feedback(String positive_feedback) {
        this.positive_feedback = positive_feedback;
    }

    public String getOffer_user() {
        return offer_user;
    }

    public void setOffer_user(String offer_user) {
        this.offer_user = offer_user;
    }
}
